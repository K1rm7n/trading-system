package com.tradingsystem.model.enums;

/**
 * Перечисление типов рыночных трендов
 */
public enum TrendType {
    /**
     * Восходящий тренд (рост цены)
     */
    UPTREND,

    /**
     * Нисходящий тренд (падение цены)
     */
    DOWNTREND,

    /**
     * Боковой тренд (цена в коридоре)
     */
    SIDEWAYS
}
